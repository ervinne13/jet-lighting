            <h1>
                Select a Role in the Left Panel to Manage
            </h1>
            <div>
                <p>Roles are named access control lists against the system modules. Each role may be assigned to any user.</p>
                <p>A role may have viewer, author, or manager access to many modules.</p>

                <ul class="text-left m-t-lg">
                    <li>
                        <strong>Viewers</strong> can view documents in the module, but cannot edit or delete any of them. They cannot create documents. This role is usually for auditors.
                    </li>
                    <li>
                        <strong>Authors</strong> can view and create any document. They may update or delete documents as long as they created that document. This role is usually for staff that manages certain parts of the organization.
                    </li>
                    <li>
                        <strong>Manager</strong> managers has full access to any documents. These are good for managers, executives, or anyone that must apply corrections to documents created by the staff.
                    </li>
                </ul>
            </div>
